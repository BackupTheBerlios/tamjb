         uint offset = (uint)_rng.Next( 0, (int)count );

         Console.WriteLine( "Choosing track: " + offset );

         string query = 
            _BuildFullQuery( criteria )
            + " LIMIT 1 OFFSET " + offset
            ;
         
         IDbCommand cmd = null;
         IDataReader reader = null;
         try
         {
            Console.WriteLine( query );

